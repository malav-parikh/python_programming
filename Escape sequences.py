# escape sequences

# using quotes inside quotes
print("I am 5'11\" tall")
print('I am 5\'11" tall')

# other escape sequenes \t \n \\
tabby_cat = "\t I'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* cat food
\t* fish
\t* catnip\n\t* grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

