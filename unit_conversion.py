# converting units 
# centimeters to inches
# pounds to kgs


my_weight = 120 # in lbs
my_height = 180 # in cm

# without conversion
print(f"My weight is {my_weight} lbs and my height is {my_height} cm")

# after conversion
print(f"My weight is {my_weight/2.20462262} kgs and my height is {my_height*0.39370079} inches")
